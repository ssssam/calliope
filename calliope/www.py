# Calliope World Wide Web integration

# How to tell what a hyperlink is pointing to.
#
# - Open Graph metadata
# - 
#
# Bandcamp albums:
# - og:title = Album, by Artist
# - og:type = album
# - og:site_name = ?
# - og:image = album cover
#
# Bandcamp artists and labels:
# - og:title = Artist
# - og:type = band
# - og:image = band image
#
# Domain isn't necessary bandcamp.com as artists can provide their own domains.
# However, album URLs always end in /album/*
#
# SoundCloud artists:
# - og:title = Artist
# - og:type = music.musician
# - og:image = band image + width&height properties
